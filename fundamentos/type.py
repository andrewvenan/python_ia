# Tipo de dados em Python

""" Em Python, existem vários tipos de dados incorporados que são usados para armazenar 
diferentes tipos de informações. Aqui estão alguns dos tipos de dados mais comuns:
1. int: Representa números inteiros, como 1, 42, -5.
2. float: Representa números de ponto flutuante, como 3.14, -0.001, 2.0.
3. str: Representa cadeias de caracteres, como "Olá", 'Python', "123".
4. bool: Representa valores booleanos, que podem ser True ou False.
"""

nome = 'Andrew'
sobrenome = 'Venancio'
idade = 32
Salario = 2150.00
status = True
print(type(nome))
print(type(sobrenome))
print(type(idade))
print(type(Salario))
print(type(status))